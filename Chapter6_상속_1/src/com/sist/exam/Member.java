package com.sist.exam;

public class Member {

	private String id;
	private String name;
	private String sex;
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getId() {
		return id;
	}
		

}
